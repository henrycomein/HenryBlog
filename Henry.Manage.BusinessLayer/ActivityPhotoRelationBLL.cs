using Henry.Manage.DataAccessLayer;
using Henry.Entity;
using Henry.Common;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Henry.Manage.BusinessLayer
{
    public class ActivityPhotoRelationBLL
    {
        #region GetData

        /// <summary>
        /// get all item with specify conditins
        /// </summary>
        /// <param name="condition">condition</param>
        /// <returns>a list width items</returns>
        public static List<ActivityPhotoRelation> GetList(ActivityPhotoRelation condition)
        {
            return ActivityPhotoRelationDAL.GetList(condition).ToEntity<ActivityPhotoRelation>();
        }

        /// <summary>
        /// get items with specify conditins,and return specified numbers of items
        /// </summary>
        /// <param name="condition">condition</param>
        /// <returns>a list width items</returns>
        /// <remarks>condition.PageSize and condition.PageIndex must be defined,then get totalitems though condition.ItemTotalCount</remarks>
        public static PageParamer<ActivityPhotoRelation> GetListWithPage(ActivityPhotoRelation condition)
        {
            int totalcount=0;
            var result= ActivityPhotoRelationDAL.GetListWithPage(condition,out totalcount).ToEntity<ActivityPhotoRelation>();;
            return new PageParamer<ActivityPhotoRelation> { Items = result,PageIndex=condition.PageIndex,PageSize=condition.PageSize, TotalCount = totalcount };
        }
        
        /// <summary>
        /// get single data
        /// </summary>
        /// <param name="keyval">primary key value</param>
        /// <returns>item</returns>
        /// <remarks>if found more than one data in database,then throws errors.</remarks>
        public static ActivityPhotoRelation GetSingleOrDefault(int keyval)
        {
            var condition = new ActivityPhotoRelation  { APR_ID = keyval };
            return  ActivityPhotoRelationDAL.GetList(condition).ToEntity<ActivityPhotoRelation>().FirstOrDefault();
        }

        #endregion

        #region handle data

        /// <summary>
        /// add data
        /// </summary>
        /// <param name="info">data infomation</param>
        /// <returns>success or fail</returns>
        public static bool Add(ActivityPhotoRelation info)
        {
            return ActivityPhotoRelationDAL.AddOrUpdate(info);
        }

        /// <summary>
        /// update data
        /// </summary>
        /// <param name="info"></param>
        /// <returns></returns>
        public static bool Update(ActivityPhotoRelation info)
        {
            return ActivityPhotoRelationDAL.AddOrUpdate(info);
        }

        /// <summary>
        /// Disable data
        /// </summary>
        /// <param name="id">data keyid</param>
        /// <returns></returns>
        public static bool Disable(int id)
        {
            return ActivityPhotoRelationDAL.UpdateStatus(id, 0); 
        }

        /// <summary>
        /// Enable data
        /// </summary>
        /// <param name="id">data keyid</param>
        /// <returns></returns>
        public static bool Enable(int id)
        {
            return ActivityPhotoRelationDAL.UpdateStatus(id, 1);
        }

        /// <summary>
        /// Delete data
        /// </summary>
        /// <param name="id">data keyid</param>
        /// <returns></returns>
        public static bool Delete(int id)
        {
            return ActivityPhotoRelationDAL.UpdateStatus(id, 2);
        }
        
        #endregion
    }
}

